          <div class="panel-heading" role="tab">
            <h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapseOne1">What is a Drum and Bugle Corps (Drum Corps)?</a></h4>
          </div>
          <div id="collapseOne1" class="panel-collapse collapse">
            <div class="panel-body">A Drum and Bugle Corps is an outdoor performance ensemble consisting of percussion, brass, and color guard sections. Sharing common roots with marching band, Drum Corps began to develop as an activity as veterans returned from WWI and WWII and sponsored local units in their communities.  As time passed these local units gradually grew more competitive and focused in their efforts, quickly becoming professional performance ensembles with elaborate shows and national tours. <br/>
              <br/>
              Just like marching band, Drum Corps perform music while executing marching formations on the field, often adding showy visual effects and utilizing props. The main difference between Drum Corps and marching band is the instrumentation; while both Corps and marching bands use battery and front ensemble percussion, Corps do not utilize woodwinds and instead feature an array of brass instruments, primarily trumpets, mellophones, baritones, euphoniums, and shoulder-mounted tubas called contras. While Drum Corps do not field woodwinds it is not uncommon for woodwind players to learn a new instrument in order to participate in the activity. </div>
          </div>
        </div>
        <div class="panel panel-default">
          <div class="panel-heading">
            <h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapseTwo1">What is Drum Corps International? </a></h4>
          </div>
          <div id="collapseTwo1" class="panel-collapse collapse">
            <div class="panel-body">Drum Corps International (DCI) is the nonprofit governing organization that manages and oversees all competitive Drum and Bugle Corps. The main functions of the organization are the management of the DCI Summer Tour, the World Championships, and the adjudication process for the activity, including the drafting and application of the rules and the training of the judges. <br/>
              <br/>
              Within DCI there are two main competitive divisions, Open and World Class. World Class is incredibly competitive and training intensive for its performing members, while Open Class is generally less competitive, with Open Class corps usually having less rehearsals and a shorter tour than their World Class counterparts. <br/>
              <br/>
              For more information on DCI, please visit <a href="http://dci.org" target="_blank">www.dci.org</a></div>
          </div>
        </div>
        <div class="panel panel-default">
          <div class="panel-heading">
            <h4 class="panel-title"><a data-toggle="collapse" data-parent="#accordion1" href="#collapseThree1">What is a SoundSport&#174 Team? </a></h4>
          </div>
          <div id="collapseThree1" class="panel-collapse collapse">
            <div class="panel-body">SoundSport is DCI’s newest program, aimed at showcasing instrumental ensembles year-round and giving those ensembles the chance to perform in exhibitions at major DCI events such as the World Championship. SoundSport’s goal is to be local and low cost, and members can be any age and perform on any instrument. Generally, SoundSport shows focus on the music rather than the visual aspect of things with the rules requiring that a show be only 5 to 7 minutes in length. <br/>
              <br/>
              For the first season, Arsenal will be fielding a SoundSport team rather than an Open Class Drum Corps in order to minimize financial strain on the organization. However, our team will still adhere to standard drum corps instrumentation and age requirements. </div>
