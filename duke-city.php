     <h3>Arsenal is happy to announce that we will be hosting the 2016 Duke City Drum-Off!</h3>
      <p> We’re excited to be sponsoring this event, which has been running for over two decades here in the city of Albuquerque. It’s a great chance for local DrumLines to throw down and for percussion students to be able to directly interact with each other in a competitive environment. We wanted to make sure that this event was the best possible experience we could offer, so we partnered with Drum Corps International to transform the DCDO into a DrumLine Battle. <br>
        <br>
        DrumLine Battle pits two DrumLines against each other in direct competition in battles consisting of two round, with each round containing around two minutes of drumming. This drumming can be anything the line wants to play- cadences, show chunks, exercises- anything, provided it’s entertaining.DrumLine Battle is all about swagger and showmanship, and the audience, just as much as the judges, determines the victor, meaning that visuals, maneuvers, and one-upping the other line is just as important as technique and skill if you want to win. <br>
        If you’re interested, you can find the full rules for DrumLine Battle here: <a href="http://www.drumlinebattle.com/wp-content/uploads/2015-DrumLine-Battle-Rulebook.pdf" target="_blank"> DrumLine Battle Rulebook </a> <br>
        <br>
        Our hope for this event is to make it as accessible as possible for everyone, both competing students and fans. As such we want to keep fees minimum, both for the audience and for students competing. DrumLines who pay before the end of October only pay $15 per student, with a unit cap of $150, so if you have a line that's bigger than ten students you still just pay $150. (For Lines who pay after October 31st, this amount increase to $20 per with a cap of $200)  Students who attend the event get in for free with a valid student ID for a competing school, and the cost for regular admission is only $5 at the door.<br>
        <br>
        We want to see as many people involved as possible with this event, and we’re excited to be able to provide a venue for the enhancement of the marching arts. There will be a number of information tables at this event, including one for Arsenal, should you be curious about our 2017 season. We’re also really happy to be supporting local groups and all concession sales will go to support the Sandia Matador Marching Band.<br>
