				<p>In selecting the repertoire, we wanted to match the dark sound of <em>Korobeiniki</em> with music of a similar texture while also holding true to our home state of New Mexico. The sultry opening verse of <em>Oh Fair New Mexico</em> fit this idea perfectly, and we begin our show with this piece and return to it in multiple forms throughout. <em>Hungarian Dance No. 5</em> kept with the dark sound we were striving for and allowed us to set the stage for a country dance wherein the young merchant might meet Katya. <em>La Llorona</em> is thus the slow song at this dance, and we chose this piece not only because it is a significant folk song from Mexico and the Southwest, but also because the specific version we chose to arrange was done by <em>Beirut</em>; an indie-rock and world music ensemble fronted by native New Mexican Zach Condon, who traveled to Mexico and recorded traditional brass bands to create the unique sound of his March of the <em>Zapotec</em> album. <br> The result is a show that is uniquely New Mexican, but will sound familiar to any audience. We are incredibly excited by how this season has been shaping up and are looking forward to our debut this summer.</p>
